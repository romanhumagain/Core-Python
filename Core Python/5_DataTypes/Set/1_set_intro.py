'''
->. Set is the collection of the unique elements
->. It Cannot have multiple occurance of the same elements.
->. It Doesn't support indexing, slicing or other sequence-like behaviour.
->. It is defined using curly braces {}
'''

fruits = {"apple", "banana", "cherry"}
print(fruits)  # {'apple', 'banana', 'cherry'}


# Using the set() methods
vegetables = set(["Roman", "Anup", "Anuj", "Pratab"])
print(vegetables)  # {'Pratab', 'Anuj', 'Roman', 'Anup'}   


# ==== Working with tuples method ====

my_tuple = (1, 2, 3, 4, 5, 2, 3, 2, 2)

# Using count() method to see how many times does the element repeated
print(f"2 is repeated {my_tuple.count(2)} times.")  

# Using the index() method to find the index of the first occured aminal
print(f"The index value of 2 is  {my_tuple.index(2)}") # Output: 1
